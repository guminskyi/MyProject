package ForLoops;

public class FL01 {
    public static void main(String[] args) {

        for (int i = 1; i >= -5; i--) {
            System.out.print(i);
            System.out.println(" Wazzup!");
        }

        for (int j = 80; j <= 100; j += 5) {
            System.out.println("Congrats - " + j);
        }

    }
}
